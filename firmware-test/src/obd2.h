/*
 * OBD-II (ISO 15765-4) опрос для легковых автомобилей.
 * В отличие от J1939, данные не идут broadcast — ECU отвечает на запросы.
 * Мы шлём 0x7DF (функциональный запрос), парсим ответы 0x7E8-0x7EF.
 * Заполняем ту же структуру VehicleData, что и J1939.
 */
#pragma once

#include <Arduino.h>
#include "j1939.h"

// ─── API ───────────────────────────────────────

// Привязать обновляемую структуру (обычно — тот же VehicleData, что у J1939).
void obd2Init(VehicleData* v);

// Периодический вызов из loop(). Сам решает, когда слать следующий запрос.
// Возвращает true, если был отправлен запрос.
bool obd2Tick();

// Вызывается из readCAN() для каждого 11-битного кадра.
// Возвращает true, если это был распознанный OBD-II ответ (0x7E8..0x7EF).
bool obd2HandleResponse(uint32_t canId, const uint8_t* data, uint8_t dlc);

// Сколько ответов получено с момента старта (для автодетекта).
uint32_t obd2ResponsesCount();

// Включить/выключить опрос (по умолчанию выключен — включается после автодетекта).
void obd2Enable(bool on);
bool obd2IsEnabled();
